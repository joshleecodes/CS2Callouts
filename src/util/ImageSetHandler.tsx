export const serveImageSet = (map: string, area: string) => {
    let imageSetObject = {
        controlImage: 'no control image',
        imageSet: ['']
    }
    
    if(map == 'MIRAGE'){
        switch(area){
            case 'A SITE - TICKET':  
                imageSetObject.controlImage = aSiteTicketControlImage;               
                imageSetObject.imageSet = [ aSiteTicketBench, aSiteTicketConnector, aSiteTicketDark1, aSiteTicketDark2, aSiteTicketDefault, aSiteTicketFirebox, aSiteTicketNinja,
                            aSiteTicketPalace, aSiteTicketRamp, aSiteTicketSandwich, aSiteTicketStairs, aSiteTicketTetris, aSiteTicketTriple, aSiteTicketWood ];
            break;
            case 'A SITE - WOOD':
                imageSetObject.controlImage = aSiteWoodControl;
                imageSetObject.imageSet = [ aSiteWoodBench, aSiteWoodConnector, aSiteWoodDefault, aSiteWoodFirebox, aSiteWoodJungle, aSiteWoodNinja, aSiteWoodSandwich,
                    aSiteWoodStairs, aSiteWoodTicket, aSiteWoodTrash, aSiteWoodTriple ];
            break;
            case 'B SITE - VAN':
                imageSetObject.controlImage = bSiteVanControl;
                imageSetObject.imageSet = [ bSiteVanApps, bSiteVanArch, bSiteVanBench, bSiteVanDefault, bSiteVanEmpty, bSiteVanKitchen, bSiteVanPlatform,
                    bSiteVanShort];
            break;
            case 'MID - WINDOW':
                imageSetObject.controlImage = midWindowControl;
                imageSetObject.imageSet = [ midWindowBench, midWindowBoxes, midWindowCat, midWindowChair, midWindowConnector, midWindowShort, midWindowTopMid];
            break;
        } 
    }

    else if(map == 'NUKE'){
        switch(area){
            case 'A SITE - HEAVEN':
                imageSetObject.controlImage = aSiteHeavenControl;
                imageSetObject.imageSet = [ aSiteHeavenBlue, aSiteHeavenCTVent, aSiteHeavenHut, aSiteHeavenMain, aSiteHeavenTetris, aSiteHeavenVent ];
            break;
            case 'B SITE - RAMP':
                imageSetObject.controlImage = bSiteRampControl;
                imageSetObject.imageSet = [ bSiteRampBoxes, bSiteRampControlRoom, bSiteRampDecon, bSiteRampDoors, bSiteRampRafter, bSiteRampYellow ];
            break;
            case 'OUTSIDE':
                imageSetObject.controlImage = outsideControl;
                imageSetObject.imageSet = [ outsideBoxes, outsideMain, outsideRed, outsideSilo, outsideYard ];
            break;
        }
    }

    else if(map == 'VERTIGO'){
        switch(area){
            case 'A SITE':
                imageSetObject.controlImage = aSiteControl;
                imageSetObject.imageSet = [ aSiteBox, aSiteCat, aSiteDefault, aSiteElevator, aSiteFence, aSiteRamp, aSiteSandbag, aSiteShort ];
            break;
            case 'B SITE':
                imageSetObject.controlImage = bSiteControl;
                imageSetObject.imageSet = [ bSiteCat, bSiteCT, bSiteDouble, bSiteGen, bSiteSandbags, bSiteStairs, bSiteTriple, bSiteWood ];
            break;
        }
    }

    return imageSetObject;
}

